// Helper functions for song filtering
export const getRandomSongs = (count = 5) => {
  const shuffled = [...karaokeLibrary].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getSongsByGenre = (genre: string) => {
  return karaokeLibrary.filter((song) => song.genre === genre);
};

export const getSongsByDifficulty = (difficulty: string) => {
  return karaokeLibrary.filter((song) => song.difficulty === difficulty);
};
